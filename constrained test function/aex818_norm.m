function [f,e,g,LB,UB]=aex818_norm(xnorm)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% Example 8_1_8 Function
%
% Author: Syed Ali Zaryab
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUTS:
%
% xo=[1,1,1,1,16,16]
% minimum at 
% x(1) = 0.771516
% x(2) =  0.516992
% x(3) =  0.204192
% x(4) =  0.388811
% x(5) =  3.03557
% x(6) =  5.09726
% obj =  -0.388811
%e is equality constraint
%g is inequality constraint
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


LB=[0,0,0,0,0.00001,0.00001];
UB=[1,1,1,1,16,16]; 
x= LB+xnorm.*(UB-LB);

x1=x(1) ;x2=x(2) ;x3=x(3) ;x4=x(4) ;x5=x(5) ;
x6=x(6); 

%minimize
f= - x4;


%Constraints


e(1)= 0.09755988*x1*x5 + x1 - 1;

e(2)= 0.0965842812*x2*x6 + x2 - x1 - 0;

e(3)= 0.0391908*x3*x5 + x3 + x1 - 1;

e(4)= 0.03527172*x4*x6 + x4 - x1 + x2 - x3 - 0;

g(1)=  x5^0.5 + x6^0.5 - 4;


end
