function [f,e,g,LB,UB]=ahouse_norm(xnorm)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% House Function
%
% Author: Syed Ali Zaryab
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUTS:
%
% xo=[30,0,0,68,0,56,0,0]
% minimum at 
% x(1) =    34.0000000000
% x(2) =    44.1176470588
% x(3) =    44.1176470588
% x(4) =    68.0000000000
% x(5) =    22.0588235294
% x(6) =    66.1764705882
% x(7) =  3000.0000000000
% x(8) =  1500.0000000000
% obj = -4500.0000000000
%e is equality constraint
%g is inequality constraint
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

LB=[0,0,0,40,0,56,0,0];
UB=[3000,3000,3000,68,3000,100,3000,3000] ;
x= LB+xnorm.*(UB-LB);

%minimize
f= - x(7) - x(8);

%Constraints
%e is equality constraint
e(1)=  - (x(1)*x(2) + x(5)*x(4)) + x(7) - 0;

e(2)=  - x(1)*x(3) + x(8) - 0;

e(3)=  - x(2) - x(5) + x(6) - 0;

e(4)=  - 0.5*x(2) + x(3) - x(5) - 0;

%g is inequality constraint 

g(1)=    -x(1) + 0.333333333333333*x(4) - 0;

g(2)=    x(1) - 0.5*x(4) - 0;

g(3)=    -x(2)*(x(4) + x(1)) + 1500;

g(4)=  +0.5*x(2) -x(5) - 0;


end