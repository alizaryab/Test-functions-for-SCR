function [f,e,g,LB,UB]=awall_norm(xnorm)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% Wall Function
%
% Author: Syed Ali Zaryab
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUTS:
%
% x0=[1,1,1,1,1,1];
% minimum at 
% x(1) =    -1.0000046624
% x(2) =    -0.9999953376
% x(3) =    -4.7977849517
% x(4) =     0.9995338714
% x(5) =    -0.9804524478
% x(6) =     1.0004663460
% obj =    -1.0000046624
%e is equality constraint
%g is inequality constraint
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

LB=[-5,-5,-5,-5,-5,-5];
UB= [5,5,5,5,5,5];

x= LB+xnorm.*(UB-LB);

%minimize
f=x(1);

%constraint
e(1)= x(1)*x(2) - 1;

e(2)= x(3)/x(1)/x(4) - 4.8;

e(3)= x(5)/x(2)/x(6) - 0.98;

e(4)= x(6)*x(4) - 1;

e(5)=    x(1) - x(2) + 1E-7*x(3) - 1E-5*x(5) - 0;

e(6)=    2*x(1) - 2*x(2) + 1E-7*x(3) - 0.01*x(4) - 1E-5*x(5) + 0.01*x(6) - 0;

g=[0];


end
