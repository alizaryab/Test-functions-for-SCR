function [y,e,g] = acolville_norm(xnorm)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% COLVILLE FUNCTION
%
% Author: Syed Ali Zaryab
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUTS:
%
% xx = [x1, x2, x3, x4]
% minimum at xx*=(1,1,1,1)  , f(xx*)= 0
% Range xx=[-10, 10] 
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
LB=[-10,-10,-10,-10];  
UB=[10,10,10,10] ; 

e=[0];
g=[0];

xx= LB+xnorm.*(UB-LB);

x1 = xx(1);
x2 = xx(2);
x3 = xx(3);
x4 = xx(4);

term1 = 100 * (x1^2-x2)^2;
term2 = (x1-1)^2;
term3 = (x3-1)^2;
term4 = 90 * (x3^2-x4)^2;
term5 = 10.1 * ((x2-1)^2 + (x4-1)^2);
term6 = 19.8*(x2-1)*(x4-1);

y = term1 + term2 + term3 + term4 + term5 + term6;

end
