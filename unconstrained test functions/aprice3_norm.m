function [y,e,g] = aprice3_norm(xnorm)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% Price 3 Function
% Author: Syed Ali Zaryab
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUTS:
%
% xx = [x1, x2]
% minimum around xx*=[0.34,0.11]  , f(xx*)= 0
% Range xx=[-5, 5] 
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
LB=[-5,-5];  
UB=[5,5] ; 

e=[0];
g=[0];

xx= LB+xnorm.*(UB-LB);

x1 = xx(1);
x2 = xx(2);


y = 100*(x2-x1^2)^2+(6.4*(x2-0.5)^2-x1-0.6)^2;

end
