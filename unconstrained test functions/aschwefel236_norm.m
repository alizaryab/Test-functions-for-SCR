function [y,e,g] = aschwefel236_norm(xnorm)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% Schwefel 2.36 Function
% 
% Author: Syed Ali Zaryab
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUTS:
%
% xx = [x1, x2]
% minimum at xx*=[12,12]  , f(xx*)= -3456
% Range xx=[0, 100] 
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
LB=[0,0];  
UB=[100,100] ; 

e=[0];
g=[0];

xx= LB+xnorm.*(UB-LB);

x1=xx(1);
x2=xx(2);

y=-x1*x2*(72-2*x1-2*x2);
end
